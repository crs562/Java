
public class Sum {

	public static void main(String[] args) {
		java.util.Scanner sc = new java.util.Scanner(System.in);
		System.out.println("enter two numbers");
		int x = sc.nextInt();
		int y = sc.nextInt();
		int res = x + y;
		System.out.println(res);
		System.out.println("Sum = " + res);
		System.out.println(x + " + " + y + " = " + res);
		
	}

}
