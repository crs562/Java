
public class FloatTest {

	public static void main(String[] args) {
		float data = 12.34F;
		System.out.println(data);
		data = (float) 12.35;
		System.out.println(data);
		
		double d = 12.34;
		System.out.println(d);
		d = 12.34D;
		System.out.println(d);
		System.out.println(Integer.toHexString(65));
	}

}
